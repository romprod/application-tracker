import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";
import { pathToFileURL } from "node:url";

import { Client } from "@modelcontextprotocol/sdk/client/index.js";
import { InMemoryTransport } from "@modelcontextprotocol/sdk/inMemory.js";
import { format } from "prettier";

const manifestPath = resolve("src/application/mcp_schema_manifest.ts");
const checkOnly = process.argv.includes("--check");

function canonicalize(value) {
  if (Array.isArray(value)) return value.map(canonicalize);
  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.keys(value)
        .sort()
        .map((key) => [key, canonicalize(value[key])]),
    );
  }
  return value;
}

function sha256(value) {
  return createHash("sha256")
    .update(JSON.stringify(canonicalize(value)))
    .digest("hex");
}

function toolContract(tool) {
  return canonicalize({
    annotations: tool.annotations,
    description: tool.description,
    execution: tool.execution,
    inputSchema: tool.inputSchema,
    name: tool.name,
    outputSchema: tool.outputSchema,
    title: tool.title,
    ...(tool._meta ? { _meta: tool._meta } : {}),
  });
}

function fail(message) {
  console.error(message);
  process.exitCode = 1;
}

const serverModule = await import(
  pathToFileURL(resolve("dist/server/server/mcp_server.js")).href
);
const applicationModule = await import(
  pathToFileURL(resolve("dist/server/application/mcp.js")).href
);
const unavailable = () => {
  throw new Error("Schema generation does not execute MCP tools");
};
const services = new Proxy({}, { get: () => unavailable });
const server = serverModule.createApplicationMcpServer(services, {
  instructions: "Schema generation",
});
const client = new Client({
  name: "application-tracker-schema-generator",
  version: "1.0.0",
});
const [clientTransport, serverTransport] = InMemoryTransport.createLinkedPair();

try {
  await server.connect(serverTransport);
  await client.connect(clientTransport);
  const listed = await client.listTools();
  const contracts = listed.tools.map(toolContract);
  const manifest = {
    schemaSha256: sha256({ tools: contracts }),
    schemaVersion: applicationModule.applicationMcpSchemaVersion,
    toolCount: contracts.length,
    tools: contracts.map((tool) => ({
      name: tool.name,
      schemaSha256: sha256(tool),
    })),
  };
  const previous = applicationModule.applicationMcpSchemaManifest;
  const contractChanged = manifest.schemaSha256 !== previous.schemaSha256;

  if (contractChanged && manifest.schemaVersion <= previous.schemaVersion) {
    fail(
      `MCP contract changed without a schema-version bump. Increase applicationMcpSchemaVersion above ${String(previous.schemaVersion)}.`,
    );
  } else if (
    !contractChanged &&
    manifest.schemaVersion !== previous.schemaVersion
  ) {
    fail("applicationMcpSchemaVersion changed without an MCP contract change.");
  } else {
    const source = await format(
      `// Generated by scripts/generate-mcp-schema.mjs. Do not edit by hand.\nexport const applicationMcpSchemaManifest = ${JSON.stringify(manifest, null, 2)} as const;\n`,
      { filepath: manifestPath },
    );
    if (checkOnly) {
      const current = await readFile(manifestPath, "utf8");
      if (current !== source) {
        fail(
          "Generated MCP schema manifest is stale. Run npm run mcp:schema:generate and commit the result.",
        );
      }
    } else {
      await writeFile(manifestPath, source);
      console.log(
        `Generated MCP schema v${String(manifest.schemaVersion)} with ${String(manifest.toolCount)} tools (${manifest.schemaSha256}).`,
      );
    }
  }
} finally {
  await client.close();
  await server.close();
}
